# erificando Aprovação em um Exame
# Para ser aprovado em um exame, um estudante precisa ter uma nota média maior
# ou igual a 7.0 e uma frequência maior ou igual a 75%.
# Dados:
# Nota média: 8.5
# Frequência: 80%
# Verifique se o estudante foi aprovado.


nota = 8.5
frequencia = 80

if nota >= 7.0 and frequencia >= 75:
    print("Aprovado")
else:
    print('Reprovado')